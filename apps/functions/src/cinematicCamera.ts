export function buildCinematicCameraScripts(
  options: CinematicCameraOptions = {},
): AdditionalScript[] {
  const opts: Required<CinematicCameraOptions> = {
    enableByDefault: options.enableByDefault ?? false,
    letterboxColor: options.letterboxColor ?? { r: 0, g: 0, b: 0 },
    hotkey: options.hotkey ?? 'F1',
  };

  return [
    {
      name: CONFIG_MODULE_NAME,
      scriptType: 'ModuleScript',
      container: 'ReplicatedStorage',
      source: buildConfigSource(opts),
    },
    {
      name: CONTROLLER_SCRIPT_NAME,
      scriptType: 'LocalScript',
      container: 'StarterPlayerScripts',
      source: buildControllerSource(),
    },
    {
      name: HUD_SCRIPT_NAME,
      scriptType: 'LocalScript',
      container: 'StarterPlayerScripts',
      source: buildHudSource(),
    },
  ];
}

/** Wraps a builder result with the cinematic camera scripts. Idempotent — if
 *  the controller is already present (e.g. nested calls) it won't double-add. */
export function withCinematicCamera(
  result: string | MultiScriptResult,
  options: CinematicCameraOptions = {},
): MultiScriptResult {
  const base: MultiScriptResult =
    typeof result === 'string'
      ? { serverScript: result, additionalScripts: [] }
      : result;

  const alreadyHas = base.additionalScripts.some(
    (s: AdditionalScript) => s.name === CONTROLLER_SCRIPT_NAME,
  );
  if (alreadyHas) return base;

  return {
    ...base,
    additionalScripts: [
      ...base.additionalScripts,
      ...buildCinematicCameraScripts(options),
    ],
  };
}
