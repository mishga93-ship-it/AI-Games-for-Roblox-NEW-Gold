function buildControllerSource(): string {
  // Lua source uses ${...} interpolation only via a tagged template later; here it
  // is a pure string so no template-literal escapes needed beyond ordinary backticks.
  return `-- CinematicCameraController.client — generated by cinematicCamera.ts (Session #179)
-- 9:16 letterbox + cinematic shot presets + REC mode.
-- Toggle: F1 (desktop) or floating mobile button (touch). One-Tap UGC capture.

local Players = game:GetService("Players")
local RunService = game:GetService("RunService")
local UserInputService = game:GetService("UserInputService")
local StarterGui = game:GetService("StarterGui")
local TweenService = game:GetService("TweenService")
local ReplicatedStorage = game:GetService("ReplicatedStorage")

local localPlayer = Players.LocalPlayer
local Config = require(ReplicatedStorage:WaitForChild("CinematicConfig"))

local cinematicMode = Config.EnabledByDefault
local activeShotName = "follow"
local orbitAngle = 0
local dollyTime = 0
local lastCFrame = nil

local function getCamera()
        return workspace.CurrentCamera
end

-- Letterbox ScreenGui: two black frames anchored top + bottom (or sides on
-- iPad-landscape). Auto-resizes via UIAspectRatioConstraint listening to
-- viewport.
local letterboxGui = Instance.new("ScreenGui")
letterboxGui.Name = "CinematicLetterbox"
letterboxGui.IgnoreGuiInset = true
letterboxGui.ResetOnSpawn = false
letterboxGui.DisplayOrder = 9999
letterboxGui.Enabled = false
letterboxGui.Parent = localPlayer:WaitForChild("PlayerGui")

local barTop = Instance.new("Frame")
barTop.Name = "BarTop"
barTop.BackgroundColor3 = Config.LetterboxColor
barTop.BorderSizePixel = 0
barTop.AnchorPoint = Vector2.new(0.5, 0)
barTop.Position = UDim2.new(0.5, 0, 0, 0)
barTop.Parent = letterboxGui

local barBottom = barTop:Clone()
barBottom.Name = "BarBottom"
barBottom.AnchorPoint = Vector2.new(0.5, 1)
barBottom.Position = UDim2.new(0.5, 0, 1, 0)
barBottom.Parent = letterboxGui

local barLeft = Instance.new("Frame")
barLeft.Name = "BarLeft"
barLeft.BackgroundColor3 = Config.LetterboxColor
barLeft.BorderSizePixel = 0
barLeft.AnchorPoint = Vector2.new(0, 0.5)
barLeft.Position = UDim2.new(0, 0, 0.5, 0)
barLeft.Parent = letterboxGui

local barRight = barLeft:Clone()
barRight.Name = "BarRight"
barRight.AnchorPoint = Vector2.new(1, 0.5)
barRight.Position = UDim2.new(1, 0, 0.5, 0)
barRight.Parent = letterboxGui

-- Compute letterbox geometry: clamp visible area to 9:16 inside actual viewport.
-- If viewport is wider than 9:16 (landscape phone, desktop) → pillarbox (left+right bars).
-- If viewport is taller than 9:16 (already portrait phone) → letterbox (top+bottom bars).
local function updateLetterbox()
        local cam = getCamera()
        if not cam then return end
        local size = cam.ViewportSize
        local viewportRatio = size.X / math.max(1, size.Y)
        local targetRatio = Config.LetterboxRatio -- 9/16 = 0.5625

        if viewportRatio > targetRatio then
                -- Pillarbox.
                local visibleWidth = size.Y * targetRatio
                local barWidth = math.max(0, (size.X - visibleWidth) / 2)
                barLeft.Size = UDim2.new(0, barWidth, 1, 0)
                barRight.Size = UDim2.new(0, barWidth, 1, 0)
                barTop.Size = UDim2.new(1, 0, 0, 0)
                barBottom.Size = UDim2.new(1, 0, 0, 0)
        else
                -- Letterbox.
                local visibleHeight = size.X / targetRatio
                local barHeight = math.max(0, (size.Y - visibleHeight) / 2)
                barTop.Size = UDim2.new(1, 0, 0, barHeight)
                barBottom.Size = UDim2.new(1, 0, 0, barHeight)
                barLeft.Size = UDim2.new(0, 0, 1, 0)
                barRight.Size = UDim2.new(0, 0, 1, 0)
        end
end

-- Hide / restore default Roblox UI (PlayerList, Chat, Backpack, EmotesMenu).
-- Keep Health visible — players still need feedback.
local hiddenCoreGuiTypes = {
        Enum.CoreGuiType.PlayerList,
        Enum.CoreGuiType.Backpack,
        Enum.CoreGuiType.Chat,
        Enum.CoreGuiType.EmotesMenu,
}
local function setCoreGuiVisible(visible)
        for _, t in ipairs(hiddenCoreGuiTypes) do
                pcall(function()
                        StarterGui:SetCoreGuiEnabled(t, visible)
                end)
        end
end

local function getRoot()
        local char = localPlayer.Character
        if not char then return nil end
        local hrp = char:FindFirstChild("HumanoidRootPart")
        return hrp
end

-- Smoothly drive camera each frame while in cinematic mode.
local function applyShot(dt)
        local hrp = getRoot()
        if not hrp then return end
        local preset = Config.ShotPresets[activeShotName] or Config.ShotPresets.follow
        local cam = getCamera()
        if not cam then return end

        local rootCF = hrp.CFrame
        local offset = preset.offset

        if activeShotName == "orbit" then
                orbitAngle = orbitAngle + dt * (preset.orbitSpeed or 0.4)
                local r = math.sqrt(offset.X * offset.X + offset.Z * offset.Z)
                offset = Vector3.new(math.cos(orbitAngle) * r, offset.Y, math.sin(orbitAngle) * r)
        elseif activeShotName == "dolly" then
                dollyTime = dollyTime + dt * (preset.dollySpeed or 1.5)
                local push = math.sin(dollyTime) * 4
                offset = offset + Vector3.new(0, 0, push)
        end

        local targetPos = rootCF.Position + Vector3.new(offset.X, offset.Y, offset.Z)
        local lookTarget = rootCF.Position + (preset.lookAtOffset or Vector3.new(0, 2, 0))
        local targetCF = CFrame.lookAt(targetPos, lookTarget)

        local alpha = math.clamp(preset.lerpAlpha or 0.1, 0.01, 1)
        if lastCFrame then
                cam.CFrame = lastCFrame:Lerp(targetCF, alpha)
        else
                cam.CFrame = targetCF
        end
        lastCFrame = cam.CFrame

        local targetFOV = preset.fov or Config.FOVCinematic
        cam.FieldOfView = cam.FieldOfView + (targetFOV - cam.FieldOfView) * alpha
end

local renderConn
local function startCinematicLoop()
        if renderConn then return end
        local cam = getCamera()
        if cam then
                cam.CameraType = Enum.CameraType.Scriptable
        end
        renderConn = RunService.RenderStepped:Connect(function(dt)
                applyShot(dt)
        end)
end

local function stopCinematicLoop()
        if renderConn then
                renderConn:Disconnect()
                renderConn = nil
        end
        local cam = getCamera()
        if cam then
                cam.CameraType = Enum.CameraType.Custom
                cam.FieldOfView = Config.FOVDefault
        end
        lastCFrame = nil
end

local function setCinematicMode(enabled)
        cinematicMode = enabled
        letterboxGui.Enabled = enabled
        setCoreGuiVisible(not enabled)
        if enabled then
                updateLetterbox()
                startCinematicLoop()
        else
                stopCinematicLoop()
        end
        -- Broadcast attribute so CinematicHud + other listeners can react.
        localPlayer:SetAttribute("CinematicMode", enabled)
end

local function cycleShot()
        local order = Config.ShotOrder
        for i, name in ipairs(order) do
                if name == activeShotName then
                        activeShotName = order[(i % #order) + 1]
                        localPlayer:SetAttribute("CinematicShot", activeShotName)
                        return
                end
        end
        activeShotName = order[1]
        localPlayer:SetAttribute("CinematicShot", activeShotName)
end

-- Public API published as attributes so HUD and game scripts can pulseFOV on
-- gameplay events (combat hits, level-ups) without coupling to this module.
function _G.CinematicPulseFOV(amount)
        local cam = getCamera()
        if not cam then return end
        local base = cinematicMode and Config.FOVCinematic or Config.FOVDefault
        local punch = amount or Config.FOVPulseAmount
        local punchTween = TweenService:Create(
                cam,
                TweenInfo.new(0.08, Enum.EasingStyle.Quad, Enum.EasingDirection.Out),
                { FieldOfView = base + punch }
        )
        local restoreTween = TweenService:Create(
                cam,
                TweenInfo.new(0.35, Enum.EasingStyle.Quad, Enum.EasingDirection.InOut),
                { FieldOfView = base }
        )
        punchTween:Play()
        punchTween.Completed:Connect(function() restoreTween:Play() end)
end

function _G.CinematicSetMode(enabled)
        setCinematicMode(enabled and true or false)
end

function _G.CinematicCycleShot()
        cycleShot()
end

-- Input bindings — accepts both Hotkey (G) and HotkeyAlt (F1) so users on any
-- platform / launcher overlay (Studio test mode often eats F1) can still toggle.
UserInputService.InputBegan:Connect(function(input, processed)
        if processed then return end
        if input.KeyCode == Config.HotkeyDesktop or input.KeyCode == Config.HotkeyDesktopAlt then
                print("[Cinematic] hotkey toggle:", input.KeyCode.Name)
                setCinematicMode(not cinematicMode)
        elseif cinematicMode and input.KeyCode == Enum.KeyCode.Tab then
                cycleShot()
        end
end)

-- Floating buttons — visible on ALL platforms (desktop, mobile, console).
-- Previously gated to TouchEnabled-only which hid the button in Studio + Mac
-- builds, leaving F1 as the only entry point. Now both work everywhere.
do
        local toggleGui = Instance.new("ScreenGui")
        toggleGui.Name = "CinematicToggleButtons"
        toggleGui.ResetOnSpawn = false
        toggleGui.DisplayOrder = 10000
        toggleGui.Parent = localPlayer:WaitForChild("PlayerGui")

        local btn = Instance.new("TextButton")
        btn.Size = UDim2.fromOffset(64, 64)
        btn.AnchorPoint = Vector2.new(1, 1)
        btn.Position = UDim2.new(1, -16, 1, -120)
        btn.BackgroundColor3 = Color3.fromRGB(20, 20, 28)
        btn.BackgroundTransparency = 0.25
        btn.AutoButtonColor = false
        btn.Text = "🎬"
        btn.TextScaled = true
        btn.TextColor3 = Color3.fromRGB(255, 255, 255)
        btn.Font = Enum.Font.GothamBold
        btn.Parent = toggleGui
        local corner = Instance.new("UICorner")
        corner.CornerRadius = UDim.new(1, 0)
        corner.Parent = btn
        local stroke = Instance.new("UIStroke")
        stroke.Thickness = 2
        stroke.Color = Color3.fromRGB(255, 80, 110)
        stroke.Parent = btn

        btn.Activated:Connect(function()
                print("[Cinematic] button toggle")
                setCinematicMode(not cinematicMode)
        end)

        local cycleBtn = btn:Clone()
        cycleBtn.Position = UDim2.new(1, -16, 1, -200)
        cycleBtn.Text = "🔄"
        cycleBtn.Parent = toggleGui
        cycleBtn.Activated:Connect(function()
                if cinematicMode then cycleShot() end
        end)
end

-- Listen for viewport resize so letterbox stays correct on rotate / resize.
local cam0 = getCamera()
if cam0 then
        cam0:GetPropertyChangedSignal("ViewportSize"):Connect(function()
                if cinematicMode then updateLetterbox() end
        end)
end

if Config.EnabledByDefault then
        setCinematicMode(true)
end

print("[CinematicCameraController] ready — press " .. Config.HotkeyDesktop.Name .. " / " .. Config.HotkeyDesktopAlt.Name .. " or tap 🎬 (bottom-right) to record cinematic clips")
`;
}

