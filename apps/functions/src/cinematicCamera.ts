// CinematicCameraController — One-Tap TikTok Gameplay Exporter (Session #179).
// Builds 3 Roblox scripts that bundle into every generated game so any player can
// toggle a 9:16 letterboxed cinematic recording mode. Captures rely on OS-level
// screen recording (iOS Control Center / Android Quick Settings / Win+G); this
// module just frames the gameplay and exposes a REC HUD with onboarding.
//
// Wired into apps/functions/src/gameTemplates.ts:9764 via withCinematicCamera().

import type { MultiScriptResult } from './gameTemplates.js';

export interface CinematicCameraOptions {
  /** Default-on letterbox? false → user must press F1 / mobile button to enable. */
  enableByDefault?: boolean;
  /** Color of letterbox bars (default black). */
  letterboxColor?: { r: number; g: number; b: number };
  /** Toggle hotkey on desktop (default F1). */
  hotkey?: 'F1' | 'F2' | 'F3' | 'F4';
}

type AdditionalScript = MultiScriptResult['additionalScripts'][number];

const CONFIG_MODULE_NAME = 'CinematicConfig';
const CONTROLLER_SCRIPT_NAME = 'CinematicCameraController';
const HUD_SCRIPT_NAME = 'CinematicHud';

